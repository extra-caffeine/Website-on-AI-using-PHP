              <p>What is Artificial Intelligence? What has Artificial Intelligence reached in the past? What are appreciable milestones of Artificial Intelligence in the last years? What will Artificial Intelligence solve in 5 years, 20 years, 50 years or 100 years from now? And how will look Artificial Intelligence the future?</p>
            <p>Artificial Intelligence (AI) we refer to a subfield of Computer Science. Artificial Intelligence is acted by machines, computers and mainly software. Machines mimic, here we see why it is called artificial, some kind of cognitive function based on environment, observations, rewards and learning process..</p>
            <p>The history of Artificial Intelligence is quite interesting and started around 100 years ago with Rossum’s Universal Robots (R.U.R.).</p>

           
            <p>In 1920 the Czech writer Karel Čapek published a science fiction play named Rossumovi Univerzální Roboti (Rossum’s Universal Robots), also better known as R.U.R. The play introduced the word robot. R.U.R. deals about a factory, which creates artificial people named as robots. They differentiate from today’s term of robot. In R.U.R. robots are living creatures, who are more similar to the term of clones. The robots in R.U.R. first worked for the humans, but then there comes are robot rebellion which leads to the extinction of the human race.</p>
