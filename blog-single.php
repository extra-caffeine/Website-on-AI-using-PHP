           <p>The play is quite interesting, because of different reason. First it is introducing the term robot, even if represents not exactly the modern idea of robots. Next it is also  telling the story of the creation of robots, so some kind of artificial intelligence, which first seems to be a positive effect to the humans, but later on the is the robot rebellion which threat the whole human race.</p>
		   
            <p>Artificial Intelligence in literature and movies is a big topic for its own. The example of R.U.R. should have shown the importance and influence for Artificial Intelligence on researches and society.</p>
			
			<h2>Alan Turning</h2>
            
			<p>Alan Turing was born on 23th June 1912 in London. He is widely known, because the encrypted the code of the enigma, which were used from Nazi Germany to communicate. Alan Turing’s study also led to his theory of computation, which deals about how efficient problems can be solved. His presented his idea in the model of the Turing machine, which is today still a popular term in Computer Science. The Turing machine is an abstract machine, which can ,despite the model’s simplicity, construct any algorithm’s logic. Because of discoveries in neurology, information theory and cybernetics in the same time researches and with them Alan Turing created the idea that it is possible to build an electronic brain.</p>
			
			<p>Some years after the end of World War 2, Turing introduced his widely known Turing Test, which was an attempt to define machines intelligent. The idea behind the test was that are machine (e.g. a computer) is then called intelligent, if a machine (A) and a person (B) communicate through natural language and a second person (C), a so-called elevator, can not detect which of the communicators (A or B) is the machine.</p>
              
            <h2>The Dartmouth conference</h2>			  
            <p>In 1956 there was probably the first workshop of Artificial Intelligence and with it the field of AI research was born. Researcher from Carnegie Mellon University (CMU), Massachusetts Institute of Technology (MIT) and employee from IBM met together and founded the AI research. </p>
			<p>That was in the 1960s. The progress slowed down in the following years. Because of the failing recognizing of the difficulty of the tasks promises were broken.</p>
			<h2>The first AI Winter</h2>
			<p>Because of the over optimistic settings and the not occurred breakthroughs U.S. and British government cut of exploratory research in AI. The following years were called (first) AI Winter. The enthusiasm was lost, nobody wanted to fund AI research. The interest of publicity on Artificial Intelligence decreased. This was around 1974.</p>
			<h2>Expert Systems</h2>
			<p>After the first AI Winter, Artificial Intelligence came back in a form of so-called “expert systems”.</p>
			<p>Expert systems are programs that answer question and solve problems in a specific domain. They emulate an expert in a specific branch and solve problems by rules. There are two types of engines in expert systems: First, there is the knowledge engine, which represents facts and rules about a specific topic. Second, there is the inference engine, which applies the rules and facts from the knowledge engine to new facts.</p>
			<h2>Synthesis of Integral Design</h2>
			<p>In 1981 an expert system named SID (Synthesis of Integral Design) designed 93% of the VAX 9000 CPU logic gates. The SID system was existing out of 1,000 hand-written-rules. The final design of the CPU took 3 hours to calculate and outperformed in many ways the human experts. As an example, the SID produced a faster 64-bit adder than the manually designed one. Also the bug per gate rate, which where around 1 bug per 200 gates from human experts, was much lower at around 1 bug per 20,000 gates at the final result of the SID system.</p>
			<h2>The second AI Winter</h2>
			<p>The second AI Winter came in the later 80s and early 90s after a series of financial setbacks. The fall of expert systems and hardware companies who suffered through desktop computers built by Apple and IBM led again to decreasing AI interest, on the one hand side from publicity and on the other side from investors./p>
